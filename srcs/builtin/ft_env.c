#include "minishell.h"

int	ft_env(int	argc, char **argv, char ***env)
{
	int i;
	(void)argv;

	i = 0;
	if (argc != 1)
		return (1);
	while ((*env)[i])
		printf("%s\n", (*env)[i++]);
	return (0);
}
